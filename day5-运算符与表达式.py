#算数运算符
'''
+  （加）、 - （减）、*  （乘）、/ （除） 这些都是最基本的，例 2+3
**  （乘方） 2**3  = 8   2的3次方等于8
//  （整除）
        x 除以 y 并对结果向下取整至最接近的整数。
        13 // 3  输出  4  。
        -13 // 3  输出  -5  。
%  （取模）
        返回除法运算后的余数。
        13 % 3  输出  1  。 -25.5 % 2.25  输出  1.5  。
'''
print(2**3) # 输出 8
print('a'*3) # 输出 aaa
print(13/3) #输出 4.33333
print(-25.5 % 2.25) #  输出 1.5
